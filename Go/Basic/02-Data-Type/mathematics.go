package main

import "fmt"

func main() {
	// mathematics operation
	var a = 20
	var b = 10
	// addition operation
	var c = a + b
	fmt.Println(c)

	// subtraction operation
	var d = a - b
	fmt.Println(d)

	// multiple operation
	var e = a * b
	fmt.Println(e)

	// division operation
	var f = a / b
	fmt.Println(f)

	// modulo operation
	var g = a % b
	fmt.Println(g)

	// augmented assignments
	var i = 10
	i += 10
	fmt.Println(i)
	
	i -= 5
	fmt.Println(i)
	
	// unary operator
	var j = 1;
	j++
	fmt.Println(j)
	
	j--
	fmt.Println(j)
}