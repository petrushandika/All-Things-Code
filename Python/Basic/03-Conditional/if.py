# if and else statement

# inline if
name = input("Whats your name? ")
if name == "ucup": print("you are so handsome!")
print(f"Thank you {name}")

# if indentation
if name == "ucup":
    print("you are so handsome!")
    print("you are so very handsome!")
print(f"Thank you {name}")

# else statement
if name == "otong":
    print("hi otong, looks cool!")
else: 
    print("ah u are not otong, not cool!")
    