# convertion temperature unit exercise

# celcius
celcius = float(input("Input temperature in celcius : "))
print("Temperature is", celcius, "Celcius")

# reamur
reamur = (4/5) * celcius
print("Temperature in reamur is", reamur, "Reamur")

# fahrenheit
fahrenheit = ((9/5) * celcius) + 32
print("Temperature in fahrenheit is", fahrenheit, "Fahrenheit")

# kelvin
kelvin = celcius + 273
print("Temperature in kelvin is", kelvin, "Kelvin")