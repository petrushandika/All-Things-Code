package Basic;

public class ComparationOperation {
    public static void main(String[] args) {
        // Operasi perbandingan -> untuk membandingkan dua nilai atau ekspresi dan menghasilkan nilai boolean (true atau false)
        int x = 10;
        int y = 5;
        // Lebih besar dari (>)
        System.out.println(x + " > " + y + " = " + (x > y));

        // Lebih kecil dari (<)
        System.out.println(x + " < " + y + " = " + (x < y));

        // Lebih besar atau sama dengan (>=)
        System.out.println(x + " >= " + y + " = " + (x >= y));

        // Lebih kecil atau sama dengan (<=)
        System.out.println(x + " <= " + y + " = " + (x <= y));

        // Sama dengan (==)
        System.out.println(x + " == " + y + " = " + (x == y));

        // Tidak sama dengan (!=)
        System.out.println(x + " != " + y + " = " + (x != y));
    }
}
